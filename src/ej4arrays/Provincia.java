public class Provincia {
    private String denominacion;
    private float sBase;

    public Provincia(String denominacion, float sBase) {
        this.denominacion = denominacion;
        this.sBase = sBase;
    }

    public String getDenominacion() {
        return denominacion;
    }

    public float getsBase() {
        return sBase;
    }
    
}
